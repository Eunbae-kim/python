#패킹과 언패킹 ( Packing & Unpacking)
t = ('foo', 'bar', 'baz', 'qux')

#패킹 튜플과 같음. 하나로 묶는 거
print(t)
print(t[0])
print(t[-1])


#언패킹
(x1,x2,x3,x4) = t #괄호가 없어도 언패킹
