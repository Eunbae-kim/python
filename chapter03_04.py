c = (11,12,13,14)
d = (100,10000,1000, 'Ace')
e = (100,1000,(100,10000,1000, 'Ace'))

#인덱싱
print('>>>>>>>>>>')
print('d = ', d[1]) #인덱싱하는 법 리스트랑 같음
print('d[1] + d[0] = ', d[1] + d[0])
print('e[2][2] = ',e[2][2])
print('e[2][2] = ',list(e[2][3])) #리스트로 형변환 시킬 수 있음

#수정이 안되는지 확인
